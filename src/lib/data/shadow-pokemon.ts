/**
 * 指定ポケモンがシャドウポケモンかどうかを判定する。
 * @param pokemonId PokeAPI形式のポケモン種族ID（例: 'makuhita'）
 * @param gameId ゲームID（'colosseum' または 'xd'）。省略時はいずれかに含まれていればtrue
 * @returns シャドウポケモンであればtrue
 */
export function isShadowPokemon(pokemonId: string, gameId?: string): boolean {
  if (gameId === 'colosseum') {
    return SHADOW_POKEMON_COLOSSEUM.includes(pokemonId);
  }
  if (gameId === 'xd') {
    return SHADOW_POKEMON_XD.includes(pokemonId);
  }
  // ゲーム指定なし: いずれかに含まれていればtrue
  return SHADOW_POKEMON_COLOSSEUM.includes(pokemonId) || SHADOW_POKEMON_XD.includes(pokemonId);
}
